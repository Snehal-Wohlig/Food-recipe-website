    <h1> Tandoori paneer tikka </h1>

   
    

     <img  src="img\Tandoori-Paneer-Tikka-2.jpg"  width="500" height="460" style="float: left;margin-right: 10px; margin-bottom: 5px"; border: solid black 1px; padding: 2px" />
        
    <p> <strong> Step 1-</strong></p>
    <p>Gather the ingredients. Mix 2 1/2 tablespoons of tandoori masala with the yogurt, 2 tablespoons cooking oil, and salt to taste, and make a smooth paste.</p>

    <p> <strong>Step 2-</strong></p>
<p>Put the paneer chunks in a bowl and pour the tandoori paste over it. Mix gently to coat the paneer. Cover and marinate in the refrigerator for two hours.</p>


<p> <strong>Step 3- </strong></p>
<p>Mix the remaining 1/2 tablespoon of tandoori masala with the onions to coat them well.Thread the marinated paneer, onion, and green and red bell pepper pieces onto bamboo skewers.</p>

<p> <strong>Step 4- Preheat your grill on medium.</strong></p>
<p>Place the paneer skewers on the hot grill and brush with a little cooking oil.Grill until the paneer is light golden and the onions are soft. Do not overcook, or the paneer will become rubbery.</p>

<p> <strong>Step 4- </strong></p>
<p>Remove onto a plate and sprinkle with chaat masala.Squeeze some lemon juice over the paneer skewers, garnish with lemon wedges, and serve hot,and Enjoy!</p>

