<p>3 tablespoons tandoori masala ( homemade or store-bought, divided)
<p>1/4 cup yogurt
<p>5 to 6 tablespoons vegetable oil (or canola or sunflower cooking oil, divided)
<p>Kosher salt (to taste)
<p>1/2 pound paneer ( homemade or store-bought, cut into 30 (2-inch) cubes)
<p>1 large onions (cut into 1-inch square pieces)
<p>1 red bell pepper (deseeded and cut into 2-inch cubes)
<p>1 green bell pepper (deseeded and cut into 2-inch cubes)
<p>2 tablespoons chaat masala (available at Indian grocery stores)
<p>Lemon juice (to taste)
<p>Garnish: lemon wedges
</p>

