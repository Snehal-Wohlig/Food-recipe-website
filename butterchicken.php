    <h1> BUTTER CHICKEN </h1>

    
 
    

     <img  src="img\Butter-Chicken-IMAGE-27.jpg"  width="500" height="500" style="float: left;margin-right: 10px; margin-bottom: 5px"; border: solid black 1px; padding: 2px" />
        
    <p> <strong> Step 1- Prepare the marinade and marinate chicken overnight</strong></p>
    <p>To prepare this mouth-watering chicken recipe, first prepare the marinade for the chicken. For the same, take a large bowl and mix together yogurt, onion paste, green chillies, ginger-garlic paste, sugar, salt, green cardamoms, black cardamoms, and mace powder. Add pieces of raw chicken in the bowl and mix well. Allow the chicken to marinate overnight. Once the chicken is well marinated, roast it in a tandoor or an oven till its 3/4th done.</p>

    <p> <strong>Step 2- Prepare the masala and then blend to make puree for the gravy</strong></p>
<p>Now, heat little butter in a pan over moderate flame. Add bay leaves, cloves, cinnamon, red chillies, and crushed coriander seeds. Sauté the ingredients for half a minute. Now in the same pan, add a bit of onion, red chilli powder, coriander powder, kasoori methi powder and tomatoes. Sauté them for 5 minutes and once done, transfer the mixture in a blender to make a puree.</p>


<p> <strong>Step 3- Cook marinated chicken in the gravy</strong></p>
<p>Heat the remaining butter in a pan. Add the pureed mixture and bring it to a boil. Add marinated chicken pieces, salt, fresh cream, and mix well. To make sure that the consistency is not too thick, add some water to the mixture. Now add sliced green chilies, crushed fenugreek leaves and let it simmer for few minutes. Bring it to a full boil.</p>

<p> <strong>Step 4- Garnish Butter Chicken with cream and serve hot</strong></p>
<p>Transfer the dish to a serving bowl and garnish it with coriander leaves and cream. This butter chicken recipe is easy-to-make and can be made by using simple ingredients available in the kitchen. If you're a spice junkie and love all things spicy, then customize this chicken recipe a bit by adding more green chillis or black pepper powder. Try it and relish the burst of Indian flavours. Serve it with naan or rice.</p>

